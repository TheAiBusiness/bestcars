/**
 * Get the actual image URL/path from a filename
 * If the image is not in the map, return the original string (for external URLs)
 */
export function getImageUrl(filename: string): string {
  return imageMap[filename] || filename;
}

/**
 * Map an array of image filenames to their actual URLs
 */
export function mapImageUrls(filenames: string[]): string[] {
  return filenames.map(getImageUrl);
}
